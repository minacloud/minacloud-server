import com.alibaba.cola.dto.Response;
import com.minacloud.common.base.query.IdQry;
import com.minacloud.upms.api.UserService;
import com.minacloud.upms.dto.cmd.UserAddCmd;
import lombok.RequiredArgsConstructor;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RestController;

@RestController
@RequiredArgsConstructor
public class UserController {
    private final UserService userService;

    @PostMapping("/user")
    public Response createUser(UserAddCmd cmd) {
        return userService.createUser(cmd);
    }

    @GetMapping("/user/{id}")
    public Response findById(@PathVariable("id") Long userId) {
        return userService.findById(IdQry.of(userId));
    }
}
